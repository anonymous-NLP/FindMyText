class Corpus:
    """Corpus of documents which can be sampled."""

    def __init__(self, jsonl_path: Optional[str] = None):
        """Initialize the Corpus by loading documents from a JSONL file."""
        self.data = []
        if jsonl_path is not None:
            with open(jsonl_path, "r") as f:
                for line in f:
                    self.data.append(orjson.loads(line))

    def sample_doc(self, min_length: int = 1000, max_length: int = 50000):
        """Sample a document from the corpus that meets the minimum length
        requirement."""
        MAX_ATTEMPTS = 1000
        attempts = 0
        while attempts < MAX_ATTEMPTS:
            doc = np.random.choice(self.data)
            doc_text = doc["text"]
            if len(doc_text) >= min_length and len(doc_text) <= max_length:
                basic_doc = {
                    k: v
                    for k, v in doc.items()
                    if k not in {"seg_langs", "web-register", "doc_scores"}
                }
                return basic_doc
            attempts += 1
        raise RuntimeError(
            f"Could not find a document of at least {min_length} characters and at most {max_length} characters after {MAX_ATTEMPTS} attempts."
        )


class PositiveExampleGenerator:
    """Generator for positive example pairs (that is, pairs of texts which do contains a
    shared segment according to the oracle) from a corpus."""

    def __init__(
        self,
        corpus: Corpus,
        other_corpus: Optional[Corpus] = None,
        min_oracle_score: float = 1000,
        target_text_length: int = 2000,
    ):
        """Initialize the PositiveExampleGenerator with a hyphenation dictionary and an
        oracle."""
        self.corpus = corpus
        if other_corpus is not None:
            self.other_corpus = other_corpus
        else:
            self.other_corpus = corpus
        self.dic = pyphen.Pyphen(lang="en")
        self.local_aligner = oracle.LocalAligner()
        self.min_oracle_score = min_oracle_score
        self.target_text_length = target_text_length

    def sample_positive(self) -> Optional[dict]:
        """Sample a positive example pair from the corpus.

        The method first samples a document from the corpus, then selects a random chunk
        of text from that document to edit. It applies a series of random edits to the
        selected chunk, while ensuring that the edited text remains sufficiently similar
        to the original text according to an oracle function. In case the edited text is
        shorter than the target length, the method expands it by concatenating random
        samples of text from the corpus.

        Returns: A dictionary containing the original text, the edited text, their pair
        type (positive), and metadata about the source document and any documents used
        for expansion.
        """
        doc = self.corpus.sample_doc(int(self.min_oracle_score * 1.1), 50000)
        doc_text = doc["text"]

        # We select a random chunk of text from the document to edit, with a minimum
        # length equal to the minimum oracle score to ensure that we have enough room
        # for edits while still retaining a shared segment that is sufficiently long.
        min_chunk_size = int(self.min_oracle_score)
        chunks = chunk_min(doc_text, min_chunk_size=min_chunk_size)
        suitable_chunks = [
            c
            for c in chunks
            if len(c) < self.min_oracle_score * 1.05 and len(c) >= min_chunk_size + 10
        ]
        if not suitable_chunks:
            print("No suitable chunks found. Skipping.")
            return None
        chunk_to_edit = np.random.choice(suitable_chunks)

        doc_id = doc.get("u", doc.get("id", "unknown"))
        print(
            "Working on chunk from %s of length %i" % (doc_id, len(chunk_to_edit)),
            flush=True,
        )

        edited_text, region_score = self.edit_text(chunk_to_edit)
        if region_score < self.min_oracle_score:
            print("\nCould not edit text:", chunk_to_edit)
            print("Final score:", region_score)
            return

        meta_data = {k: v for k, v in doc.items() if k not in {"text"}}

        # If the edited text is shorter than the target length, we expand it by concatenating random
        # samples of text from the corpus before and after it, until the target length is reached.
        edited_text, expansion_meta = self.expand_to_target_length(edited_text)

        result = {
            "text1": doc_text,
            "text2": edited_text,
            "pair_type": "positive",
            "score": region_score,
            "meta": meta_data,
            **expansion_meta,
        }
        return result

    def expand_to_target_length(self, text: str) -> Tuple[str, Dict[str, Any]]:
        """If the input text is shorter than the target length, expand it by
        concatenating random samples of text from the corpus before and after it, until
        the target length is reached.

        The method also returns metadata about the documents used for expansion.
        """
        meta_data = {"before": []}
        expanded_text = text

        # If the text is shorter than the target length, we add a preamble text from another document.
        while len(expanded_text) < self.target_text_length:
            nb_to_add = self.target_text_length - len(expanded_text)
            preamble_doc = self.other_corpus.sample_doc()
            preamble_text = preamble_doc["text"]
            if len(preamble_text) > nb_to_add:
                preamble_text = chunk_min(preamble_text, min_chunk_size=nb_to_add)[0]
            expanded_text = preamble_text + " " + expanded_text
            meta_data["before"].append(
                {"doc_id": preamble_doc["id"], "added": preamble_text}
            )
            print("Added preamble text of length %i" % len(preamble_text))

        return expanded_text, meta_data

    def edit_text(self, text, nb_attempts=500, timeout=300) -> Tuple[str, float]:
        """Apply a series of random edits to the input text, while ensuring that the
        edited text remains sufficiently similar to the original text according to an
        oracle function.

        The edits include adding hyphenation, changing casing, adding boilerplate
        segments, and switching normalisation of unicode characters. The function
        returns the final edited text after applying a series of edits that are accepted
        by the oracle.

        Args:
            text: The input text to edit.
            nb_attempts: The maximum number of edit attempts to apply to the text.
            timeout: The maximum time in seconds to spend on editing the text.

        Returns:
            A tuple containing the final edited text and its oracle score compared to the original text.

        """

        initial_text = str(text)
        successive_edits = []

        init_time = time.time()
        for _ in range(nb_attempts):
            if time.time() - init_time > timeout:
                break
            edit_functions = [
                self.add_hyphenation,
                self.change_casing,
                self.add_boilerplate_segment,
                self.switch_normalisation,
            ]
            chosen_edit_function = edit_functions[
                np.random.randint(len(edit_functions))
            ]
            edited_text = chosen_edit_function(text)
            if edited_text == text:
                continue

            print(".", end="", flush=True)
            region_score = self.local_aligner.get_largest_region_score(
                initial_text, edited_text
            )

            if region_score < self.min_oracle_score:
                if len(successive_edits) > 0:
                    print("OK", flush=True)
                    return successive_edits[-1]
                else:
                    continue

            successive_edits.append((edited_text, region_score))
            text = edited_text

        print("KO", flush=True)
        return initial_text, 0.0

    def add_hyphenation(self, text, nb_to_hyphenate=2) -> str:
        """Hyphenates a few words based on actual linguistic syllable boundaries."""
        words = text.split()

        # Shuffle words to avoid always picking the first eligible word
        indices = np.arange(len(words))
        np.random.shuffle(indices)

        hyphenated_words = 0
        for idx in indices:
            word = words[idx]

            # Strip punctuation for cleaner processing
            clean_word = word.strip('.,!?;:()"')
            if len(clean_word) < 5:  # Short words rarely wrap
                continue

            # Get hyphenation positions (e.g., 'hyphenation' -> 'hy-phen-ation')
            hyphenated_style = self.dic.inserted(clean_word)

            if "-" in hyphenated_style:
                # Choose a random hyphenation point provided by the dictionary
                parts = hyphenated_style.split("-")
                split_idx = np.random.randint(1, len(parts))

                head = "".join(parts[:split_idx])
                tail = "".join(parts[split_idx:])

                # Randomly decide whether to insert a newline after the hyphen to mimic line breaks in PDFs
                if random.random() > 0.5:
                    new_word = f"{head}-{tail}"
                else:
                    new_word = f"{head}-\n{tail}"

                # Replace only the specific instance of the word
                words[idx] = word.replace(clean_word, new_word)
                hyphenated_words += 1

                if hyphenated_words >= nb_to_hyphenate:
                    return " ".join(words)

        return text  # Return original if no suitable word found

    def change_casing(self, text, nb_to_change=3) -> str:
        """Randomly changes the casing of a few words in the text."""
        words = text.split()

        nb_changed = 0
        while nb_changed < nb_to_change:
            # In 50% of cases, we change the casing of a titled word to lowercase
            if np.random.rand() < 0.5:
                titled_indices = [i for i, w in enumerate(words) if w.istitle()]
                if titled_indices:
                    idx = np.random.choice(titled_indices)
                    words[idx] = words[idx].lower()
                    nb_changed += 1
            # Otherwise, we change the casing of a lowercase word to either uppercase or title case
            else:
                lowercase_indices = [i for i, w in enumerate(words) if w.islower()]
                if lowercase_indices:
                    idx = np.random.choice(lowercase_indices)
                    if np.random.rand() < 0.5:
                        words[idx] = words[idx].upper()
                    else:
                        words[idx] = words[idx].title()
                    nb_changed += 1

        return " ".join(words)

    def add_boilerplate_segment(self, text) -> str:
        """Add a short boilerplate string at a random position in the text, to mimic
        text that could have been extracted from a PDF or a crawled web page."""
        segment = np.random.choice(BOILERPLATE_SEGMENTS)
        words = text.split()
        insert_idx = np.random.randint(0, len(words) + 1)
        words.insert(insert_idx, segment)
        return " ".join(words)

    def switch_normalisation(self, text) -> str:
        """Switch the normalisation of some unicode characters, such as changing
        straight quotes to curly quotes, or changing hyphens to en-dashes.

        Only reverse one type of character at a time.
        """
        transformations = [
            (r'"(.*?)"', r"“\1”"),  # Straight quotes to curly quotes
            (r"“(.*?)”", r'"\1"'),  # Curly quotes to straight quotes
            (r"'(.*?)'", r"‘\1’"),  # Straight single quotes to curly single quotes
            (r"‘(.*?)’", r"'\1'"),  # Curly single quotes to straight single quotes
            (r"'", r"’"),  # ASCII apostrophe to right single quotation mark
            (r"’", r"'"),  # Right single quotation mark to ASCII apostrophe
            (r"--", r"—"),  # Double hyphens to em-dash
            (r"—", r"--"),  # em-dash to double hyphens
            (r"-", r"–"),  # Hyphens to en-dash
            (r"–", r"-"),  # En-dash to hyphens
            (r"\.\.\.", r"…"),  # Three dots to ellipsis
            (r"…", r"..."),  # Ellipsis to three dots
            (r"fi", r"ﬁ"),  # fi ligature
            (r"ﬁ", r"fi"),  # fi ligature expanded
            (r"fl", r"ﬂ"),  # fl ligature
            (r"ﬂ", r"fl"),  # fl ligature expanded
            (r"\(c\)", "©"),  # Copyright symbol
            (r"©", "(c)"),  # Copyright symbol
            (r"\(r\)", "®"),  # Registered trademark symbol
            (r"®", "(r)"),  # Registered trademark symbol
            (r"\(tm\)", "™"),  # Trademark symbol
            (r"™", "(tm)"),  # Trademark symbol
        ]

        order = np.random.permutation(len(transformations))
        for idx in order:
            pattern, replacement = transformations[idx]
            matches = list(re.finditer(pattern, text))
            if not matches:
                continue

            match = matches[np.random.randint(len(matches))]
            replaced = re.sub(pattern, replacement, match.group(0), count=1)
            return text[: match.start()] + replaced + text[match.end() :]

        return text


class NegativeExampleGenerator:
    """Generator for negative example pairs (that is, pairs of texts which do not contain a
    shared segment according to the oracle) from a corpus."""

    def __init__(
        self,
        corpus: Corpus,
        llm_model: Optional[str] = "Qwen/Qwen2-7B-Instruct",
        min_oracle_score: float = 1000,
    ):
        """Initialize the NegativeExampleGenerator with a corpus and an LLM model."""
        self.corpus = corpus
        if llm_model is None:
            self.model = None
        else:
            import vllm

            self.model = vllm.LLM(
                model=llm_model,
                max_num_batched_tokens=3000,
                max_model_len=3000,
                max_num_seqs=1,
                gpu_memory_utilization=0.80,
                tensor_parallel_size=1,
            )
        self.aligner = oracle.LocalAligner()
        self.min_oracle_score = min_oracle_score

    def sample_negative(self, max_nb_attempts=100, debug=False) -> dict:
        """Sample a negative example from the corpus by randomly selecting a document
        and editing it, either by paraphrasing it using the LLM or by randomly permuting
        the order of a few chunks of the text. The method applies a series of edits to
        the selected chunk, while ensuring that the edited text is sufficiently
        different from the original text according to an oracle function.

        Returns: A dictionary containing the original text, the edited text, their pair
        type (negative), and metadata about the example.
        """

        # We sample a document from the corpus that is long enough to allow for editing
        # but not too long to avoid excessively long processing times.
        doc = self.corpus.sample_doc(int(self.min_oracle_score * 2.0), 30000)

        initial_text = str(doc["text"])
        initial_text_nobreaks = re.sub(r"\s+", " ", initial_text).strip()
        print("Working on text", doc["id"], flush=True)
        if debug:
            print("Original text:", initial_text_nobreaks)

        cur_score = len(initial_text_nobreaks)
        cur_text = str(initial_text_nobreaks)

        step_nr = 1
        nb_consecutive_skips = 0
        while step_nr <= max_nb_attempts and cur_score > self.min_oracle_score:
            print("STEP %i:" % (step_nr), flush=True)

            # We identify the region of the text that will have the maximum impact on the oracle score if edited
            edit_start, edit_end = self._get_region_to_edit(
                initial_text_nobreaks, cur_text, max_chunk_size=None
            )
            chunk_to_edit = cur_text[edit_start:edit_end]

            # We decide whether to paraphrase or permute the selected region:
            # a) If no LLM model is available, we default to permutation.
            # b) If we are in the second half of the maximum attempts, we do paraphrasing
            # c) Otherwise, we do permutation if the region is more than twice the minimum oracle score,
            # and paraphrasing otherwise.
            if self.model is None or nb_consecutive_skips >= 2:
                operation = "permutation"
            elif step_nr > max_nb_attempts / 2:
                operation = "paraphrasing"
            elif edit_end - edit_start > (self.min_oracle_score * 2):
                operation = "permutation"
            else:
                operation = "paraphrasing"

            if operation == "permutation":
                print("Permutation of region [%i:%i]" % (edit_start, edit_end))
                edited_chunk = self.permute_chunks(chunk_to_edit)

            # Otherwise, we paraphrase a smaller chunk of the region using the LLM
            else:
                edit_start, edit_end = self._get_region_to_edit(
                    initial_text_nobreaks, cur_text, max_chunk_size=500
                )
                print("Paraphrasing of region [%i:%i]" % (edit_start, edit_end))
                chunk_to_edit = cur_text[edit_start:edit_end]
                edited_chunk = self.get_paraphrase(chunk_to_edit)

            # We construct the new text by replacing the selected region with the edited chunk
            parts = [
                cur_text[:edit_start],
                edited_chunk,
                cur_text[edit_end:],
            ]
            new_text = " ".join(parts)
            new_text = re.sub(r"\s+", " ", new_text).strip()

            if debug:
                print("After edit:", new_text)

            # We compute the oracle score of the new text compared to the original text
            new_score = self.aligner.get_largest_region_score(
                initial_text_nobreaks, new_text
            )
            print("==> Region score after edit: %.1f" % new_score)
            step_nr += 1

            # If the new score is higher than the current score, we skip this edit and keep the previous text
            if new_score > cur_score:
                print("WARNING: Region score increased after edit, skipping step")
                nb_consecutive_skips += 1
                continue
            else:
                cur_score = new_score
                cur_text = new_text
                nb_consecutive_skips = 0

        if cur_score >= self.min_oracle_score:
            raise RuntimeError(
                "Example pair still above threshold after %i paraphrasing/permutation attempts, aborting."
                % max_nb_attempts
            )

        print(
            "Finished editing doc %s after %i paraphrasing steps. Final score: %.1f"
            % (doc["id"], step_nr - 1, cur_score)
        )
        if debug:
            print("Final text:", cur_text)

        result = {
            "text1": initial_text,
            "text2": cur_text,
            "pair_type": "negative",
            "score": cur_score,
            "meta": {k: v for k, v in doc.items() if k not in {"text"}},
        }
        return result

    def continue_negative(
        self, doc_incr: int, max_nb_attempts=150, debug=False
    ) -> dict:
        """Assuming self.corpus is a corpus of negative examples, continue editing the text through
        paraphrasing."""

        doc = self.corpus.data[doc_incr]

        initial_text = str(doc["source_text"])
        initial_text_nobreaks = re.sub(r"\s+", " ", initial_text).strip()

        print("Working on text", doc["source_doc_id"], flush=True)
        if debug:
            print("Original text:", initial_text_nobreaks)

        cur_score = doc["oracle_score"]
        cur_text = str(doc["text"])

        step_nr = 1
        while step_nr <= max_nb_attempts and cur_score >= self.min_oracle_score:
            print("STEP %i:" % (step_nr), flush=True)

            # We identify the region of the text that will have the maximum impact on the oracle score if edited
            edit_start, edit_end = self._get_region_to_edit(
                initial_text_nobreaks, cur_text, max_chunk_size=400
            )

            print("Paraphrasing of region [%i:%i]" % (edit_start, edit_end))
            chunk_to_edit = cur_text[edit_start:edit_end]
            edited_chunk = self.get_paraphrase(chunk_to_edit)

            # We construct the new text by replacing the selected region with the edited chunk
            parts = [
                cur_text[:edit_start],
                edited_chunk,
                cur_text[edit_end:],
            ]
            new_text = " ".join(parts)
            new_text = re.sub(r"\s+", " ", new_text).strip()

            if debug:
                print("After edit:", new_text)

            # We compute the oracle score of the new text compared to the original text
            new_score = self.aligner.get_largest_region_score(
                initial_text_nobreaks, new_text
            )
            print("==> Region score after edit: %.1f" % new_score)
            step_nr += 1

            # If the new score is higher than the current score, we skip this edit, but add a boilerplate
            # segment somewhere in the region that was edited, to reduce the score in the next step.
            if new_score > cur_score:
                print(
                    "WARNING: Region score increased after edit, skipping step, and adding boilerplate"
                )
                segment_to_insert = np.random.choice(BOILERPLATE_SEGMENTS)
                insertion_point = np.random.randint(edit_start, edit_end)
                while cur_text[insertion_point] != " ":
                    insertion_point += 1
                cur_text = (
                    cur_text[:insertion_point]
                    + " "
                    + segment_to_insert
                    + cur_text[insertion_point:]
                )
                continue
            else:
                cur_score = new_score
                cur_text = new_text

        if cur_score >= self.min_oracle_score:
            raise RuntimeError(
                "Example pair still above threshold after %i paraphrasing/permutation attempts, aborting."
                % max_nb_attempts
            )

        print(
            "Finished editing doc %s after %i paraphrasing steps. Final score: %.1f"
            % (doc["source_doc_id"], step_nr - 1, cur_score)
        )
        if debug:
            print("Final text:", cur_text)

        result = {
            "text1": initial_text,
            "text2": cur_text,
            "score": cur_score,
            "meta": {"id": doc["source_doc_id"]},
        }
        return result

    def _get_region_to_edit(
        self,
        initial_text: str,
        edited_text: str,
        max_chunk_size: Optional[int] = None,
        nb_retries=5,
    ) -> Tuple[int, int]:
        """Identify a region of the edited text that fullfills the following criteria:
        - it belongs to the region of high similarity with the initial text
        - it starts and ends at sentence boundaries (if possible)
        - it is at most max_chunk_size characters long.

        Args:
            initial_text: The original text before editing.
            edited_text: The edited text after paraphrasing or permutation.
            max_chunk_size: The maximum number of characters for the edit region.
            nb_retries: The number of retries to find a suitable region if the initial attempt fails.

        Returns:
            A tuple (start, end) representing the start and end indices of the region to edit.

        """

        alignment_result = self.aligner.run(initial_text, edited_text)
        if not alignment_result.regions:
            return (0, 0)

        largest_region = max(alignment_result.regions, key=lambda r: r["score"])
        region_start = largest_region["start2"]
        region_end = largest_region["end2"]

        if region_start >= region_end:
            return (region_start, region_start)

        # Pick a random starting point within the aligned region
        if max_chunk_size is not None:
            max_random_start = max(region_start, region_end - max_chunk_size)
            edit_start = np.random.randint(region_start, max_random_start + 1)
        else:
            edit_start = region_start

        # Move start to first sentence boundary within the aligned region, if any.
        m = re.search(r"[.!?]", edited_text[edit_start:region_end])
        if m:
            edit_start = edit_start + m.end()
            while edit_start < region_end and edited_text[edit_start].isspace():
                edit_start += 1

        # Ensure edit_start doesn't exceed region_end
        edit_start = min(edit_start, region_end)

        if max_chunk_size is not None:
            hard_end = min(edit_start + max_chunk_size, region_end)
        else:
            hard_end = region_end

        # Move end to last sentence boundary in [edit_start, hard_end], if any.
        subtext = edited_text[edit_start:hard_end]
        punct_matches = list(re.finditer(r"[.!?]", subtext))
        if punct_matches:
            edit_end = edit_start + punct_matches[-1].end()
        else:
            edit_end = hard_end

        if (
            edit_end > edit_start + 30
        ):  # Ensure the region is at least 30 characters long
            return (edit_start, edit_end)
        elif nb_retries > 0:
            print("WARNING: Could not find a suitable region to edit. trying again...")
            return self._get_region_to_edit(
                initial_text,
                edited_text,
                max_chunk_size=max_chunk_size,
                nb_retries=nb_retries - 1,
            )
        else:
            if max_chunk_size is not None:
                return (region_start, min(region_start + max_chunk_size, region_end))
            else:
                return (region_start, region_end)

    def get_paraphrase(self, chunk: str) -> str:
        """Use the LLM to generate a paraphrase of the input chunk of text, by prompting it
        with instructions to produce a lightly edited version of the input text that
        retains the same meaning but with slightly different words."""

        if self.model is None:
            raise RuntimeError("LLM model not initialized for paraphrasing.")

        # We set the sampling parameters
        import vllm

        sampling_params = vllm.SamplingParams(
            temperature=0.8, top_p=0.8, top_k=50, max_tokens=600, n=1
        )

        # We add the chunk to the prompt
        prompt = PROMPT_TEMPLATE.format(text=chunk)

        # We call the model
        outputs = self.model.chat(
            [{"role": "user", "content": prompt}],
            sampling_params,
            chat_template_kwargs={"enable_thinking": False},
        )

        # We extract the model's response.
        responses = [
            outputs[0].outputs[i].text.strip() for i in range(len(outputs[0].outputs))
        ]

        for response in responses:
            if len(response) < len(chunk) * 0.3:
                print("WARNING: Paraphrased text is too short. Using original chunk.")
                return chunk
            elif len(response) > len(chunk) * 2.5:
                print("WARNING: Paraphrased text is too long. Using original chunk.")
                print("ORIGINAL:", chunk)
                print("PARAPHRASED:", response)
                return chunk
            else:
                return response

        raise RuntimeError(
            "Unexpected error in parsing model response for paraphrasing."
        )

    def permute_chunks(self, text):
        """Randomly permute the order of a few chunks of the input text."""
        # We sample chunk proportions from a Dirichlet distribution to determine the relative sizes of the chunks

        nb_chunks = np.random.randint(2, 9)  # Split into 2 to 8 chunks
        chunk_proportions = np.random.dirichlet(np.ones(nb_chunks), size=1)[0]

        # We use the chunk_proportion method to split the text into chunks of approximately
        # the specified relative sizes
        chunks = chunk_proportion(text, chunk_proportions=chunk_proportions)

        print(
            "Permuting %i chunks of sizes: %s (target proportions: %s)"
            % (len(chunks), [len(c) for c in chunks], str(chunk_proportions))
        )

        # We randomly permute the order of the chunks
        np.random.shuffle(chunks)

        return " ".join(chunks)


def chunk_min(text, min_chunk_size=400):
    """Split the input text into chunks of a specified size, ensuring that splits occur
    at sentence boundaries when possible.

    Each chunk should be at least min_chunk_size characters long.
    """
    if min_chunk_size >= len(text):
        raise ValueError(
            "min_chunk_size must be smaller than the length of the input text."
        )

    sentences = re.split(r"(?<=[.!?]) +", text)
    chunks = []
    current_chunk = ""
    for sentence in sentences:
        current_chunk += " " + sentence if current_chunk else sentence
        if len(current_chunk) > min_chunk_size:
            chunks.append(current_chunk.strip())
            current_chunk = ""

    if current_chunk:
        if chunks:
            chunks[-1] += " " + current_chunk.strip()
        else:
            print("WARNING: single chunk of size", len(current_chunk))
            chunks.append(current_chunk.strip())

    return chunks


def chunk_max(text, max_chunk_size=400):
    """Split the input text into chunks of a specified size, ensuring that splits occur
    at sentence boundaries when possible.

    Each chunk should be at most max_chunk_size characters long.
    """
    sentences = re.split(r"(?<=[.!?\n]) +", text)
    chunks = []
    current_chunk = ""
    for sentence in sentences:
        new_chunk = current_chunk + " " + sentence if current_chunk else sentence
        if current_chunk and len(new_chunk) > max_chunk_size:
            chunks.append(current_chunk.strip())
            current_chunk = sentence
        else:
            current_chunk = new_chunk

    if current_chunk:
        chunks.append(current_chunk.strip())

    return chunks


def chunk_proportion(text, chunk_proportions: List[float]):
    """Split the input text into chunks, where the approximate size of each chunk is
    determined by the corresponding value in chunk_proportions. The method ensures that
    splits occur at sentence boundaries when possible.

    Args:
        text: The input text to be chunked.
        chunk_proportions: A list of relative sizes for each chunk. The values should sum to 1.

    Returns:
        A list of text chunks corresponding to the specified relative sizes.

    """
    if not np.isclose(sum(chunk_proportions), 1.0, atol=1e-2):
        raise ValueError("The values in chunk_proportions should sum to 1.")

    sentences = re.split(r"(?<=[.!?]) +", text)
    total_length = len(text)
    target_chunk_sizes = [total_length * rel_size for rel_size in chunk_proportions]

    chunks = []
    current_chunk = ""
    current_chunk_idx = 0

    for sentence in sentences:
        if current_chunk_idx >= len(target_chunk_sizes):
            chunks.append(current_chunk.strip())
            current_chunk = sentence
            continue

        new_chunk = current_chunk + " " + sentence if current_chunk else sentence
        if len(new_chunk) > target_chunk_sizes[current_chunk_idx]:
            chunks.append(current_chunk.strip())
            current_chunk = sentence
            current_chunk_idx += 1
        else:
            current_chunk = new_chunk

    if current_chunk:
        chunks.append(current_chunk.strip())

    return chunks


def create_negative_samples(
    indexed_samples_path: str,
    min_oracle_score: float = 1000,
    output_path: str = "negative_samples_1000.jsonl",
    overwrite_existing: bool = False,
    use_llm: bool = True,
):
    """Create negative samples (in relation to a corpus of indexed texts) and save them
    to a JSONL file.

    The method continuously samples negative examples using the NegativeExampleGenerator
    and appends them to the output file.

    Args:
        indexed_samples_path: Path to the JSONL file containing the indexed samples
      (the corpus from which the negative examples will be generated).
        min_oracle_score: Minimum oracle score required for a sample to be considered
        a valid negative example.
        output_path: Path to the JSONL file where the generated negative samples
      will be saved.
        overwrite_existing: If True, the method will overwrite the output
        use_llm: whether to use an LLM for paraphrasing, or just rely on permutations
      file if it already exists. If False, it will append to the existing file.

    """
    corpus = Corpus(indexed_samples_path)

    if use_llm:
        neg_gen = NegativeExampleGenerator(corpus, min_oracle_score=min_oracle_score)
    else:
        neg_gen = NegativeExampleGenerator(
            corpus, llm_model=None, min_oracle_score=min_oracle_score
        )

    if os.path.exists(output_path) and overwrite_existing:
        os.remove(output_path)
    print("Storing negative samples in %s" % output_path)

    while True:
        print("Start sampling negative example pair")
        try:
            neg_example = neg_gen.sample_negative()
            result = {
                "text": neg_example["text2"],
                "source_doc_id": neg_example["meta"]["id"],
                "source_text": neg_example["text1"],
                "type": "negative",
                "oracle_score": neg_example["score"],
            }

            with filelock.FileLock(f"{output_path}.lock"):
                with open(output_path, "a") as f:
                    f.write(orjson.dumps(result).decode("utf-8") + "\n")
                    print("DONE")

        except Exception as e:
            print(f"Error occurred while sampling negative example: {e}")


def continue_negative_samples(
    input_negative_samples_path: str,
    min_oracle_score: float = 1000,
    output_path: str = "negative_samples_1000.jsonl",
    overwrite_existing: bool = False,
    max_nb_attempts: int = 150,
):
    """Create negative samples (in relation to a corpus of indexed texts) and save them
    to a JSONL file.

    The method continuously samples negative examples using the NegativeExampleGenerator
    and appends them to the output file.

    Args:
        input_negative_samples_path: Path to the JSONL file containing the existing negative samples
      (the corpus from which the negative examples will be generated).
        min_oracle_score: Minimum oracle score required for a sample to be considered
        a valid negative example.
        output_path: Path to the JSONL file where the generated negative samples
      will be saved.
        max_nb_attempts: Maximum number of attempts to generate a valid negative example.
        overwrite_existing: If True, the method will overwrite the output
      file if it already exists. If False, it will append to the existing file.

    """
    corpus = Corpus(input_negative_samples_path)

    neg_gen = NegativeExampleGenerator(corpus, min_oracle_score=min_oracle_score)

    if os.path.exists(output_path) and overwrite_existing:
        os.remove(output_path)
    print("Storing negative samples in %s" % output_path)

    source_doc_ids_already_done = set()
    if not overwrite_existing and os.path.exists(output_path):
        with open(output_path, "r") as f:
            for line in f:
                try:
                    sample = orjson.loads(line)
                    source_doc_ids_already_done.add(sample["source_doc_id"])
                except Exception as e:
                    print(f"Error occurred while reading existing output file: {e}")

    doc_indices_to_do = list(range(len(corpus.data)))
    random.shuffle(doc_indices_to_do)

    for doc_idx in doc_indices_to_do:
        if corpus.data[doc_idx]["source_doc_id"] in source_doc_ids_already_done:
            print(
                f"Skipping doc {corpus.data[doc_idx]['source_doc_id']} as it is already done."
            )
            continue

        print("Continue editing negative example pair")
        try:
            neg_example = neg_gen.continue_negative(
                doc_idx, max_nb_attempts=max_nb_attempts
            )
            result = {
                "text": neg_example["text2"],
                "source_doc_id": neg_example["meta"]["id"],
                "source_text": neg_example["text1"],
                "type": "negative",
                "oracle_score": neg_example["score"],
            }

            with filelock.FileLock(f"{output_path}.lock"):
                with open(output_path, "a") as f:
                    f.write(orjson.dumps(result).decode("utf-8") + "\n")
                    print("DONE")

                with open(output_path, "r") as f:
                    for line in f:
                        try:
                            sample = orjson.loads(line)
                            source_doc_ids_already_done.add(sample["source_doc_id"])
                        except Exception as e:
                            print(
                                f"Error occurred while reading existing output file: {e}"
                            )

        except Exception as e:
            print(f"Error occurred while sampling negative example: {e}")


def create_positive_samples(
    indexed_samples_path: str,
    non_indexed_samples_path: str,
    negative_samples_path: str,
    min_oracle_score: float = 1000,
    output_path: str = "positive_samples_1000.jsonl",
    overwrite_existing: bool = False,
):
    """Create positive samples (in relation to a corpus of indexed texts) and save them
    to a JSONL file.

    The method continuously samples positive examples using the PositiveExampleGenerator
    and appends them to the output file.

    Args:
        indexed_samples_path: Path to the JSONL file containing the indexed samples
      (the corpus from which the positive examples will be generated).
        non_indexed_samples_path: Path to the JSONL file containing non-indexed samples
      (used for expanding the positive examples to reach the target length).
        negative_samples_path: Path to the JSONL file containing negative samples
      (used for determining the target length).
        min_oracle_score: Minimum oracle score required for a sample to be considered a
      valid positive example.
        output_path: Path to the JSONL file where the generated positive samples will be saved.
        overwrite_existing: If True, the method will overwrite the output file if it already exists.
      If False, it will append to the existing file.

    """
    corpus = Corpus(indexed_samples_path)
    other_corpus = Corpus(non_indexed_samples_path)

    with open(negative_samples_path, "r") as f:
        negative_samples = [orjson.loads(line) for line in f]
    target_length = int(np.median([len(sample["text"]) for sample in negative_samples]))
    print(f"Target length to emulate: {target_length}")

    if os.path.exists(output_path) and overwrite_existing:
        os.remove(output_path)

    gen = PositiveExampleGenerator(
        corpus=corpus,
        other_corpus=other_corpus,
        min_oracle_score=min_oracle_score,
        target_text_length=target_length,
    )
    count = 0
    while True:
        edited_sample = gen.sample_positive()
        if edited_sample is None:
            continue

        # We save the edited text, the source document ID, and the full source
        # document text in the output JSONL file.
        result = {
            "text": edited_sample["text2"],
            "source_doc_id": edited_sample["meta"]["id"],
            "source_text": edited_sample["text1"],
            "type": "positive",
            "oracle_score": edited_sample["score"],
        }

        # Adding information about the documents used for expansion, if applicable
        for x in ["before", "after"]:
            if x in edited_sample:
                result[x] = edited_sample[x]

        with filelock.FileLock(f"{output_path}.lock"):
            with open(output_path, "a") as f2:
                f2.write(orjson.dumps(result).decode("utf-8") + "\n")

        count += 1
        if count % 100 == 0:
            print(f"Generated {count} positive samples so far.")
        if count >= 10000:
            break
