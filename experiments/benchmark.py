"""Code for sampling positive and negative example pairs from a corpus of documents, in
order to evaluate the performance of a fingerprint-based methods for text containment.

The examples are deliberately sampled to be "difficult":

1) The positive examples are generated by applying a series of random edits to a chunk
of text from a document, while ensuring that the edited text still contain a substantial
shared segment according to an oracle function. The edits include adding hyphenation,
changing casing, adding boilerplate segments, and switching normalisation of unicode
characters. If the edited text is shorter than a target length, it is expanded by
concatenating random samples of text from the corpus before and after it, until the
target length is reached.

2) The negative examples are generated by randomly selecting a document and editing it,
either by paraphrasing it using an LLM or by randomly permuting the order of a few
chunks of the text. The method applies a series of edits to the selected chunk, while
ensuring that the edited text is sufficiently different from the original text according
to an oracle function. The paraphrasing is done by prompting an LLM to produce a lightly
edited version of the input text that retains the same meaning but with slightly
different words. The permutation is done by splitting the text into chunks and randomly
shuffling their order.
"""

